// Nodemon

// - It is a package/tool used to automatically restart server with the code changes

// Features:-

// Auto-Restart: Automatically restarts Node.js app on file changes.
// Developer Experience: Improves dev experience by eliminating manual restarts

//---------------------------------------------------------------------------------------------------------------------------------------

// Installation

//   `npm install -g nodemon`       //<- We always install nodemon globally

//---------------------------------------------------------------------------------------------------------------------------------------

// Usage:

// Run with:-       `nodemon filename.js`

//---------------------------------------------------------------------------------------------------------------------------------------

// Benefits:-

// Productivity: Saves time by automating server restarts.
// Real-time Feedback: Instantly reflects code changes.
// Debugging: Simplifies debugging with automatic restarts.
// Collaboration: Ensures team works with latest code effortlessly.

//---------------------------------------------------------------------------------------------------------------------------------------
