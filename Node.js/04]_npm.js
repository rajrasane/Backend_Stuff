// NPM (Node Package Manager)

// - npm is the standard package manager for Node.js

//---------------------------------------------------------------------------------------------------------------------------------------

// Uses
// - to store library of packages
// - using as command line tool to let other developers access the packages

//---------------------------------------------------------------------------------------------------------------------------------------

// Installing Packages

// npm install <-packagename->

// 1) Node Modules
// - the node_modules folder contains every installed dependancy for your project

// 2) package-lock.json
// - It records the exact version of every installed dependancy , including its sub-dependancies and their versions 

// 3) package.json
// - this file contains descriptive and functional metadata about a project , such as a name,version and dependencies

//---------------------------------------------------------------------------------------------------------------------------------------

// - Don't include the `node_modules` directory in your GitHub repository.
// - Only include the `package.json` file.
// - The `node_modules` directory and its files can be regenerated based on the information in `package.json` and either `package-lock.json`.

//---------------------------------------------------------------------------------------------------------------------------------------

// Steps for installing `node_modules` (Windows)

// 1:- npm init
// 2:- npm i package_name
// 3:- npm i node

